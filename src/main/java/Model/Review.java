package Model;

/**
 *
 * @author admin
 */
public class Review {

    private String comment;
    private int star;

    public Review(String comment, int star) {
        this.comment = comment;
        this.star = star;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public int getStar() {
        return star;
    }

    public void setStar(int star) {
        if (star >= 0 && star <= 5) {
            this.star = star;
        }
    }

}
